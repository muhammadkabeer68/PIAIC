# Slicing
fruits = ['apple', 'banana', 'cherry', 'date', 'elderberry']
print(fruits[1:3])  
print(fruits[1:])  
print(fruits[:3])  

# Append
fruits = ['apple', 'banana', 'cherry']
fruits.append('date')
print(fruits)  

# Extend
fruits = ['apple', 'banana', 'cherry']
fruits.extend(['date', 'elderberry'])
print(fruits)  

# Insert
fruits = ['apple', 'banana', 'cherry']
fruits.insert(1, 'date')
print(fruits)  

# Remove
fruits = ['apple', 'banana', 'cherry']
fruits.remove('banana')
print(fruits) 

# Pop
